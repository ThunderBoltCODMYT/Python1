#Python Turtle Graphics Example: Star
import turtle
sc=turtle.Screen()
sc.setup(400,300)
sc.bgcolor("white")
sc.title("Welcome to turtle window")
T=turtle.Turtle()
T.forward(100)
T.left(120)
T.forward(100)
T.left(120)
T.forward(100)
T.penup()
T.right(150)
T.forward(50)
T.pendown()
T.right(90)
T.forward(100)
T.right(120)
T.forward(100)
T.right(120)
T.forward(100)
sc.exitonclick()